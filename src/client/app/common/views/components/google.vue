<template>
<div class="mk-google">
	<input type="search" v-model="query" :placeholder="q" readonly>
	<button @click="search">
		<span style="color:#4285f4">G</span><span style="color:#ea4335">o</span><span style="color:#fbbc05">o</span><span style="color:#ea4335">g</span><span style="color:#34a853">l</span><span style="color:#ea4335">e</span><span>検索</span>
	</button>
</div>
</template>

<script lang="ts">
import Vue from 'vue';
import i18n from '../../../i18n';

export default Vue.extend({
	i18n: i18n(),
	props: ['q'],
	data() {
		return {
			query: null
		};
	},
	mounted() {
		this.query = this.q;
	},
	methods: {
		search() {
			const engine = 'https://www.google.com/search?q={{query}}';
			const url = engine.replace('{{query}}', encodeURIComponent(this.query))
			window.open(url, '_blank');
		}
	}
});
</script>

